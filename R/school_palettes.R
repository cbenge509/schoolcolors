#' A School Palette Generator
#'
#' These are the color palettes from NCAA schools.
#'
#' @param name Name of desired palette.
#' @param n Number of colors desired. Many schools only have only a handful of
#'   colors. If omitted, uses all colors.
#' @param type One of "all", "primary", or "secondary". "all" will return all
#'   possible colors, "primary" will return just the main colors for a school,
#'   and "secondary" will return all other available colors other than "primary".
#' @param scale Either "discrete" or "continuous". Use continuous if you want
#'   to automatically interpolate between colors.
#' @return A vector of colors.
#' @export
#' @keywords colors
school_palette <- function(name, n, type = c("all", "primary", "secondary"), scale = c("discrete", "continuous")) {
  type <- match.arg(type)
  scale <- match.arg(scale)

  pal <- school_palettes[which(names(school_palettes) %in% name)]

  if (length(pal) > 0) {
    pal <- unlist(pal, recursive = FALSE, use.names = FALSE)

  } else {
    stop("Palette not found.")
  }

  pal <- switch(type,
                all = unlist(pal, use.names = FALSE),
                primary = pal[[1]],
                secondary = if(length(pal) == 2) pal[[2]] else stop("Palette not found."))

  if (missing(n)) {
    n <- length(pal)
  }

  if (scale == "discrete" && n > length(pal)) {
    stop("Number of requested colors greater than what palette can offer")
  }

  out <- switch(scale,
                continuous = grDevices::colorRampPalette(pal)(n),
                discrete = pal[1:n]
  )
  structure(out, class = "palette", name = name)
}

#' @export
#' @importFrom graphics rect par image text
#' @importFrom grDevices rgb
print.palette <- function(x, ...) {
  n <- length(x)
  old <- par(mar = c(0.5, 0.5, 0.5, 0.5))
  on.exit(par(old))

  image(1:n, 1, as.matrix(1:n), col = x,
        ylab = "", xaxt = "n", yaxt = "n", bty = "n")

  rect(0, 0.9, n + 1, 1.1, col = rgb(1, 1, 1, 0.8), border = NA)
  text((n + 1) / 2, 1, labels = attr(x, "name"), cex = 1, family = "serif")
}
